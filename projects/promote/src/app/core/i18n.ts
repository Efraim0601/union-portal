const DICTS: Record<Lang, Dict> = { fr: FR, en: EN };

@Injectable({ providedIn: 'root' })
export class I18n {
  readonly lang = signal<Lang>((localStorage.getItem(LANG_KEY) as Lang) || 'fr');
  readonly label = computed(() => (this.lang() === 'fr' ? 'FR' : 'EN'));

  t = (key: string, vars?: Record<string, string | number>): string => {
    let s = DICTS[this.lang()][key] ?? key;
    if (vars) for (const k of Object.keys(vars)) s = s.replace(`{${k}}`, String(vars[k]));
    return s;
  };

  toggle() {
    const next: Lang = this.lang() === 'fr' ? 'en' : 'fr';
    this.lang.set(next);
    localStorage.setItem(LANG_KEY, next);
  }
}
